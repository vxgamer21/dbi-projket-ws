package com.example.zellervandecastellpatientenverwaltung.domain;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class TelefonNummerTest {
    @Test
    void when_telefonnummer_is_null_throws_exception(){
        TelefonNummer.TelefonNummerException ex = assertThrows(TelefonNummer.TelefonNummerException.class, () -> new TelefonNummer(null,null, null, TelefonNummerArt.FESTNETZ));
        assertEquals("TelefonNummer values must not be null", ex.getMessage());
    }

}